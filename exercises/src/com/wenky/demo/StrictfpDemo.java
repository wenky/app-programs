public class StrictfpDemo {
    public static void main(String[] args) {

        System.out.println("1.0f/0="+(1.0f/0));
        System.out.println("-1.0f/0="+(-1.0f/0));
        System.out.println("0.0f/0="+(0.0f/0));


        System.out.println("0/0="+(0/0));
        System.out.println("1/0="+(1/0));
        System.out.println("-1/0="+(-1/0));
    }
}
